<?php

namespace GOLEM\GolemFaucetBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * UserCheck
 *
 * @ORM\Table(name="user_check")
 * @ORM\Entity(repositoryClass="GOLEM\GolemFaucetBundle\Repository\UserCheckRepository")
 */
class UserCheck
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="ip", type="string", length=255)
     */
    private $ip;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set ip
     *
     * @param string $ip
     *
     * @return UserCheck
     */
    public function setIp($ip)
    {
        $this->ip = $ip;

        return $this;
    }

    /**
     * Get ip
     *
     * @return string
     */
    public function getIp()
    {
        return $this->ip;
    }
}

